    public void disableButton() {
        if (reading == 1) {
            readingButton.setEnabled(false);
        }
        
        if (reaction == 1) {
            reactionButton.setEnabled(false);
        }
        
        if (reading == 1 & reaction == 1) {
            reactionButton.setEnabled(true);
            readingButton.setEnabled(true);
            reading = 0;
            reaction = 0;
        }
    }
    
